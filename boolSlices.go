package slices

import "math/rand"

//BoolShuffle will randomly swap the bool elements of a slice.
func BoolShuffle(sp *[]bool) {
	slice := *sp
	rand.Shuffle(len(slice), func(i, j int) {
		slice[i], slice[j] = slice[j], slice[i]
	})
}

//BoolEquals compares two bool slices. Returns true if their elements are equal.
func BoolEquals(a, b []bool) bool {
	if a == nil && b == nil {
		return true
	}
	if a == nil || b == nil {
		return false // one of them is nil, the other is not.
	}
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
