package slices

import "math/rand"

//UintShuffle will randomly swap the uint elements of a slice.
func UintShuffle(sp *[]uint) {
	slice := *sp
	rand.Shuffle(len(slice), func(i, j int) {
		slice[i], slice[j] = slice[j], slice[i]
	})
}

//UintEquals compares two uint slices. Returns true if their elements are equal.
func UintEquals(a, b []uint) bool {
	if a == nil && b == nil {
		return true
	}
	if a == nil || b == nil {
		return false // one of them is nil, the other is not.
	}
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
