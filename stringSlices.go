package slices

import "math/rand"

//StringShuffle will randomly swap the string elements of a slice.
func StringShuffle(sp *[]string) {
	slice := *sp
	rand.Shuffle(len(slice), func(i, j int) {
		slice[i], slice[j] = slice[j], slice[i]
	})
}

//StringEquals compares two string slices. Returns true if their elements are equal.
func StringEquals(a, b []string) bool {
	if a == nil && b == nil {
		return true
	}
	if a == nil || b == nil {
		return false // one of them is nil, the other is not.
	}
	if len(a) != len(b) {
		return false
	}
	for i := range a {
		if a[i] != b[i] {
			return false
		}
	}
	return true
}
